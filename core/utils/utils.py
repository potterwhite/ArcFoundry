def ensure_dir(path):
    if not os.path.exists(path):
        os.makedirs(path)


def timed_input(prompt, timeout=30, default='y'):
    """
    Waits for user input with a countdown. Returns default if timeout.
    Works on Linux/Unix systems.
    """
    sys.stdout.write(f"{prompt} (Timeout: {timeout}s, Default: {default.upper()}): ")
    sys.stdout.flush()

    # Monitor sys.stdin for input
    ready, _, _ = select.select([sys.stdin], [], [], timeout)

    if ready:
        user_input = sys.stdin.readline().strip().lower()
        return user_input if user_input else default
    else:
        sys.stdout.write(f"\n[TIMEOUT] No input received. Auto-selecting default: {default.upper()}\n")
        return default


def cleanup_garbage(target_dir=".", patterns=None):
    """
    Cleans up temporary/garbage files matching specific patterns.

    Args:
        target_dir (str): Directory to search in. Defaults to current CWD.
        patterns (list): List of glob patterns. Defaults to RKNN junk files.
    """
    if patterns is None:
        # Default junk generated by RKNN C++ backend
        patterns = ["check*.onnx", "encoder.processed.*", "*.rknn_util_Config"]

    if 'logger' in globals():
        logger.info(f"\nðŸ§¹ Cleaning up junk files in {target_dir}...")

    for pattern in patterns:
        search_path = os.path.join(target_dir, pattern)

        found_files = glob.glob(search_path)

        for f in found_files:
            try:
                if os.path.exists(f):
                    os.remove(f)
                    if 'logger' in globals():
                        # Use global logger if available
                        # global means who invoked this function has already setup logging
                        logger.info(f"ðŸ§¹ Cleaned up junk file: {f}")
            except OSError as e:
                if 'logger' in globals():
                    logger.error(f"Failed to delete file {f}: {str(e)}")

    if 'logger' in globals():
        logger.info(f"\nðŸ§¹ Cleanup complete.\n")


def get_sdk_version():
    """
    Simple parser to get version from pyproject.toml (SSOT).
    """
    try:
        # Locate project root (assuming core/utils.py is in core/)
        root_dir = os.path.dirname(os.path.dirname(os.path.dirname(os.path.abspath(__file__))))
        toml_path = os.path.join(root_dir, "pyproject.toml")

        if os.path.exists(toml_path):
            with open(toml_path, 'r') as f:
                for line in f:
                    if line.strip().startswith("version"):
                        return line.split('=')[1].strip().strip('"').strip("'")
    except Exception:
        pass
    return "unknown"


def setup_logging(verbose=False):
    """
    Configure logging with version info and verbosity control.
    """
    ver = get_sdk_version()

    # Determine Log Level
    log_level = logging.DEBUG if verbose else logging.INFO

    # Create Formatter
    # formatter = logging.Formatter(
    #     fmt=f"[ArcFoundry v{ver}] %(asctime)s [%(levelname)s] %(message)s",
    #     datefmt="%H:%M:%S"
    # )
    formatter = SmartNewlineFormatter(fmt=f"[ArcFoundry v{ver}] %(asctime)s [%(levelname)s] %(message)s",
                                      datefmt="%H:%M:%S")

    # Setup Root Logger
    root_logger = logging.getLogger()
    root_logger.setLevel(log_level)

    # Avoid duplicate handlers if re-initialized
    if not root_logger.handlers:
        console_handler = logging.StreamHandler(sys.stdout)
        console_handler.setFormatter(formatter)
        root_logger.addHandler(console_handler)

    # Suppress noisy libraries
    logging.getLogger("urllib3").setLevel(logging.WARNING)
    logging.getLogger("onnxruntime").setLevel(logging.WARNING)

    return logging.getLogger("ArcFoundry")


# Initialize a default logger instance for module-level usage
logger = setup_logging(verbose=False)
